"""Shared data-transfer objects used across agents and services.

Every piece of data that flows between agents is defined here so that
the contracts are explicit, validated, and serialisable to JSON for
storage and API responses.
"""

from __future__ import annotations

import uuid
from dataclasses import dataclass, field
from datetime import datetime
from enum import Enum
from typing import Any


# ── Enums ────────────────────────────────────────────────────────────────────


class TaskType(str, Enum):
    """Supported evaluation task categories."""

    SUMMARIZATION = "summarization"
    QA = "qa"
    CODING = "coding"
    REASONING = "reasoning"


class ExperimentStatus(str, Enum):
    """Lifecycle states for an experiment."""

    PENDING = "pending"
    RUNNING = "running"
    COMPLETED = "completed"
    FAILED = "failed"


# ── Data-transfer objects ────────────────────────────────────────────────────


@dataclass
class EvalTask:
    """A single evaluation prompt generated by TaskGeneratorAgent."""

    task_id: str = field(default_factory=lambda: str(uuid.uuid4()))
    task_type: TaskType = TaskType.SUMMARIZATION
    prompt: str = ""
    reference: str = ""
    dataset_name: str = ""
    dataset_split: str = "test"
    sample_index: int = 0
    metadata: dict[str, Any] = field(default_factory=dict)


@dataclass
class ModelOutput:
    """Raw output from a single model on a single task."""

    task_id: str = ""
    model_id: str = ""
    output_text: str = ""
    latency_seconds: float = 0.0
    input_tokens: int = 0
    output_tokens: int = 0
    error: str | None = None
    timestamp: datetime = field(default_factory=datetime.utcnow)
    metadata: dict[str, Any] = field(default_factory=dict)


@dataclass
class EvalResult:
    """Scored evaluation result produced by the EvaluatorAgent."""

    task_id: str = ""
    model_id: str = ""
    scores: dict[str, float] = field(default_factory=dict)   # e.g. {"rouge1": 0.42, "bleu": 0.31}
    latency_seconds: float = 0.0
    estimated_cost_usd: float = 0.0
    error: str | None = None
    metadata: dict[str, Any] = field(default_factory=dict)


@dataclass
class LeaderboardEntry:
    """Aggregated ranking entry for a single model."""

    model_id: str = ""
    rank: int = 0
    avg_quality_score: float = 0.0
    avg_latency_seconds: float = 0.0
    total_cost_usd: float = 0.0
    metric_breakdown: dict[str, float] = field(default_factory=dict)
    num_tasks: int = 0


@dataclass
class ExperimentReport:
    """Final output of the evaluation pipeline."""

    experiment_id: str = field(default_factory=lambda: str(uuid.uuid4()))
    task_type: TaskType = TaskType.SUMMARIZATION
    dataset_name: str = ""
    models: list[str] = field(default_factory=list)
    leaderboard: list[LeaderboardEntry] = field(default_factory=list)
    detailed_results: list[EvalResult] = field(default_factory=list)
    status: ExperimentStatus = ExperimentStatus.PENDING
    created_at: datetime = field(default_factory=datetime.utcnow)
    completed_at: datetime | None = None
    error: str | None = None
    metadata: dict[str, Any] = field(default_factory=dict)
